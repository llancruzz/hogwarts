import "@testing-library/jest-dom";

/*
Connect mock service worker:
Create a server instance with all the handlers defined in handlers.js
Start the server before all tests are run.
Reset the handlers after each test.
Colse the server after all the tests are run.
*/
import { setupServer } from "msw/node";
import { handlers } from "./mocks/handlers";

const server = setupServer(...handlers);

beforeAll(() => server.listen());
afterEach(() => server.resetHandlers());
afterAll(() => server.close());
